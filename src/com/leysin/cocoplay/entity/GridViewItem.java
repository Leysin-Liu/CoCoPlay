package com.leysin.cocoplay.entity;

public class GridViewItem {
	
	public int picId;
	
	public String name;
	
	public int number;
	
	public int getPicId() {
		return picId;
	}

	public void setPicId(int picId) {
		this.picId = picId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}


	
}
